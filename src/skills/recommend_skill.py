def _normalize_scene_type(scene_type: str) -> str:
    """标准化场景类型"""
    scene_type = scene_type.lower().strip()

    # 精确匹配
    for std_type, keywords in SCENIC_TYPES.items():
        if scene_type in [t.lower() for t in keywords] or scene_type == std_type:
            return std_type

    # 关键词匹配
    for std_type, keywords in SCENIC_TYPES.items():
        for keyword in keywords:
            if keyword in scene_type:
                return std_type

    return None


def _find_similar_types(scene_type: str) -> List[str]:
    """查找相似类型"""
    similar = []
    scene_lower = scene_type.lower()

    for std_type in SCENIC_TYPES:
        if (
            scene_lower in SCENIC_TYPES[std_type]
            or scene_type in SCENIC_TYPES[std_type]
        ):
            similar.append(std_type)

    return similar
